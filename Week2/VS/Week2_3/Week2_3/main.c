#include <stdio.h>

int main(void) {
	char c;
	scanf("%c", &c);

	//''로 표현한 char형 데이터는 ASCII 코드에 따라 대소 비교가 가능하다.
	printf("%d\n", (c >= 'a') && (c <= 'z'));

	return 0;
}